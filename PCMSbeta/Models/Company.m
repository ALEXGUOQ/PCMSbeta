#import "Company.h"


@implementation Company

@dynamic companyId;
@dynamic name;
/**
 *  获得查询结果
 *
 *  @param predicate 条件
 *
 *  @return NSFetchedResultsController
 */
+ (NSFetchedResultsController *)fetchedResultsControllerWithPredicate:(NSPredicate *)predicate {
    return [EntityUtil fetchedResultsControllerWithEntityName:@"Company" sortProperty:@"name" predicate:predicate];
}

@end
